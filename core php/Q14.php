<!-- Color Selector: Write a program to display the name of a color based on user input -->
<!-- (red, green, blue). -->

<?php 
 $color = "red";
    switch ($color) {
        case "red":
            echo "The color is red.";
            break;
        case "green":
                echo "The color is green.";
                break;
        case "blue":
                echo "The color is blue.";
                break;
        default:
               echo "Invalid color.";
                break;
    }


?>